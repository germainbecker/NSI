CREATE TABLE Album (id_album INTEGER PRIMARY KEY AUTOINCREMENT,
                    titre VARCHAR(100),
                    annee INT,
                    dispo INT);

CREATE TABLE Artiste (id_artiste INTEGER PRIMARY KEY AUTOINCREMENT,
					  nom VARCHAR(100),
					  prenom VARCHAR(100));

CREATE TABLE Artiste_de (id_artiste INT REFERENCES Artiste(id_artiste),
						 id_album INT REFERENCES Album(id_album),
						 PRIMARY KEY (id_artiste, id_album));

CREATE TABLE Client (id_client INTEGER PRIMARY KEY AUTOINCREMENT,
					 nom VARCHAR(100),
					 prenom VARCHAR(100),
					 email VARCHAR(100));

CREATE TABLE Emprunt (id_client INT REFERENCES Client(id_client),
                      id_album INT REFERENCES Album(id_album),
					  jour DATE,
					  PRIMARY KEY (id_client, id_album));


